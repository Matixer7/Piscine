char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		j;
	char	*p;
	char	*q;

	p = dest;
	q = src;
	i = 0;
	j = 0;
	while (*p++ && src[0])
		i++;
	while (*q++)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	return (dest);
}
